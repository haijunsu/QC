public class PrinceScore {

	private int id;

	private String forest;

	private String mountain;

	private String river;

	private long turnaroundTime;


	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return the forest
	 */
	public String getForest() {
		return forest;
	}

	/**
	 * @param forest the forest to set
	 */
	public void setForest(String forest) {
		this.forest = forest;
	}

	/**
	 * @return the mountain
	 */
	public String getMountain() {
		return mountain;
	}

	/**
	 * @param mountain the mountain to set
	 */
	public void setMountain(String mountain) {
		this.mountain = mountain;
	}

	/**
	 * @return the river
	 */
	public String getRiver() {
		return river;
	}

	/**
	 * @param river the river to set
	 */
	public void setRiver(String river) {
		this.river = river;
	}

	/**
	 * @return the turnaroundTime
	 */
	public long getTurnaroundTime() {
		return turnaroundTime;
	}

	/**
	 * @param turnaroundTime the turnaroundTime to set
	 */
	public void setTurnaroundTime(long turnaroundTime) {
		this.turnaroundTime = turnaroundTime;
	}


}
