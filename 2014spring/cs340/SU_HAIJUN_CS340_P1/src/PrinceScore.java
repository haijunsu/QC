public class PrinceScore {

	private int id;

	private int forest;

	private int mountain;

	private int river;

	private int thinking;

	private long turnaroundTime;

	private int bouns;

	/**
	 * @return the bouns
	 */
	public int getBouns() {
		return bouns;
	}

	/**
	 * @param bouns the bouns to set
	 */
	public void setBouns(int bouns) {
		this.bouns = bouns;
	}

	/**
	 * @return the bouns
	 */
	public void addBouns(int bouns) {
		this.bouns += bouns;
	}

	/**
	 * @return the turnaroundTime
	 */
	public long getTurnaroundTime() {
		return turnaroundTime;
	}

	/**
	 * @param turnaroundTime the turnaroundTime to set
	 */
	public void setTurnaroundTime(long turnaroundTime) {
		this.turnaroundTime = turnaroundTime;
	}

	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return the forest
	 */
	public int getForest() {
		return forest;
	}

	/**
	 * @param forest the forest to set
	 */
	public void setForest(int forest) {
		this.forest = forest;
	}

	/**
	 * @return the mountain
	 */
	public int getMountain() {
		return mountain;
	}

	/**
	 * @param mountain the mountain to set
	 */
	public void setMountain(int mountain) {
		this.mountain = mountain;
	}

	/**
	 * @return the river
	 */
	public int getRiver() {
		return river;
	}

	/**
	 * @param river the river to set
	 */
	public void setRiver(int river) {
		this.river = river;
	}

	/**
	 * @return the thinking
	 */
	public int getThinking() {
		return thinking;
	}

	/**
	 * @param thinking the thinking to set
	 */
	public void setThinking(int thinking) {
		this.thinking = thinking;
	}



}
